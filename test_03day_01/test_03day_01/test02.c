static int g_val = 2021;
//加了static关键字之后，只能在自己所在的源文件内部使用，出了源文件，就无法链接到
//全局变量是具有外部链接属性的
//其他源文件通过声明可以使用
//static修饰全局变量后，把外部链接属性，改成了内部链接属性

