//函数加static修饰之后，跟全局变量一样
//依然改变了函数的链接属性
//使得函数的外部链接属性，变成了内部链接属性
//被修饰的函数只能在自己所在的源文件内部使用
static int Add(int x, int y)
{
	return x + y;
}